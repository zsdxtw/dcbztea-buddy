export const PROVINCE_NAMES: string[] = REGIONS.map(r => r.name);

/** 根据省份获取城市列表 */
export function getCities(province: string): CityData[] {
  return REGIONS.find(r => r.name === province)?.cities ?? [];
}

/** 根据省份获取城市名称列表 */
export function getCityNames(province: string): string[] {
  return getCities(province).map(c => c.name);
}

/** 根据省份和城市获取区县列表 */
export function getDistricts(province: string, city: string): string[] {
  return getCities(province).find(c => c.name === city)?.districts ?? [];
}
