const INITIALS = 'ABCDEFGHJKLMNOPQRSTWXYZ';
const BOUNDARIES = '阿八嚓哒鹅发旮哈讥咔垃妈拿噢啪七日撒他挖希压匝';

/** 获取汉字的拼音首字母 */
export function getPinyinInitial(char: string): string {
  const ch = char.charAt(0);
  if (/[a-zA-Z]/.test(ch)) return ch.toUpperCase();
  if (!/[\u4e00-\u9fff]/.test(ch)) return '';

  for (let i = BOUNDARIES.length - 1; i >= 0; i--) {
    if (ch.localeCompare(BOUNDARIES[i], 'zh') >= 0) {
      return INITIALS[i];
    }
  }
  return '';
}

