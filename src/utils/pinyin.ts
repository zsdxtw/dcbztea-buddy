/** 反向查找表：汉字 -> 拼音 */
const PINYIN_MAP: Record<string, string> = {};
for (const [pinyin, chars] of Object.entries(PINYIN_SOURCE)) {
  for (const ch of chars) {
    if (!PINYIN_MAP[ch]) {
      PINYIN_MAP[ch] = pinyin;
    }
  }
}

/** 获取汉字的完整拼音，找不到时回退到首字母小写 */
export function getFullPinyin(char: string): string {
  const ch = char.charAt(0);
  if (/[a-zA-Z]/.test(ch)) return ch.toLowerCase();
  if (!/[\u4e00-\u9fff]/.test(ch)) return '';
  return PINYIN_MAP[ch] || getPinyinInitial(ch).toLowerCase();
}

/**
 * 生成平台编码
 * 规则：PT-XX，XX为平台简称的首字母
 * 如有首字母重复（编码冲突），平台简称第一个字用全拼
 */
export function generatePlatformCode(shortName: string, existingCodes: string[]): string {
  if (!shortName) return 'PT-';

  const chars = Array.from(shortName);

  // 提取每个字的首字母
  const initials = chars.map(ch => getPinyinInitial(ch)).filter(Boolean).join('');
  const baseCode = `PT-${initials}`;

  // 检查是否冲突
  if (!existingCodes.includes(baseCode)) {
    return baseCode;
  }

  // 冲突时，第一个字用全拼，其余用首字母
  const firstFull = getFullPinyin(chars[0]);
  const restInitials = chars.slice(1).map(ch => getPinyinInitial(ch)).filter(Boolean).join('');
  return `PT-${firstFull}${restInitials}`.toUpperCase();
}
